__author__ = 'aluno01'

import math

numFloat = 1.23545648561687

numInt = 25

#arredonda para cima
print(math.ceil(numFloat))

#arredonda para baixo
print(math.floor(numFloat))

#fatorial
print(math.factorial(numInt))

#retorna true caso seja letra e False caso seja numero
print(math.isnan(numInt))

#checa se positivo ou negativo
print(math.isinf(numInt))

#retorna x*(2**i)
print(math.ldexp(3, 2))

#potenciação
print(math.pow(3, 2))

#radiciação (raiz quadrada)
print(math.sqrt(numInt))

#coseno
print(math.cos(90))

#seno
print(math.sin(90))

#tangente
print(math.tan(90))

#pi
print(math.pi)